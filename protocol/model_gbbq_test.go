// ───────── 仿射复权 vs 通达信桌面端 实盘核对 ─────────
//
// 数据来源: 通达信不复权日线(.day) + gbbq category=1 事件;
// 期望值取自通达信桌面客户端前复权显示值(已人工核对)。
// 复权模型为仿射 price_qfq = round_half_up(QFQMul*raw + QFQAdd)。
//
// 覆盖: 纯派现 / 送股+派现 / 配股(peigujia>0) / 大比例送转 / 含停牌的股改复合事件。

type fixKline struct {
	date       int // YYYYMMDD
	o, h, l, c int // 价格, 单位厘(元*1000)
}

type fixEvent struct {
	date             int
	fh, pj, song, pg float64
}

func buildXRXDs(evs []fixEvent) XRXDs {
	xs := make(XRXDs, 0, len(evs))
	for _, e := range evs {
		xs = append(xs, &XRXD{
			Time:        fixDate(e.date),
			Fenhong:     e.fh,
			Peigujia:    e.pj,
			Songzhuangu: e.song,
			Peigu:       e.pg,
		})
	}
	return xs
}

func buildKlines(ks []fixKline) []*Kline {
	out := make([]*Kline, 0, len(ks))
	for _, k := range ks {
		out = append(out, &Kline{
			Time:  fixDate(k.date),
			Open:  Price(k.o),
			High:  Price(k.h),
			Low:   Price(k.l),
			Close: Price(k.c),
			Last:  Price(k.c),
		})
	}
	return out
}

func fixDate(yyyymmdd int) time.Time {
	y := yyyymmdd / 10000
	m := (yyyymmdd / 100) % 100
	d := yyyymmdd % 100
	return time.Date(y, time.Month(m), d, 15, 0, 0, 0, time.Local)
}

func factorByDate(fs []*Factor) map[int]*Factor {
	m := make(map[int]*Factor, len(fs))
	for _, f := range fs {
		key := f.Time.Year()*10000 + int(f.Time.Month())*100 + f.Time.Day()
		m[key] = f
	}
	return m
}

const naN = -999999.0

func approxYuan(got Price, expYuan float64) bool {
	d := got.Float64() - expYuan
	if d < 0 {
		d = -d
	}
	return d < 0.005
}

func TestFactors_QFQ_TDX(t *testing.T) {
	type assertion struct {
		date       int
		o, h, l, c float64 // 期望前复权 开/高/低/收(元), naN 表示不校验该字段
		note       string
	}
	cases := []struct {
		name    string
		klines  []fixKline
		events  []fixEvent
		asserts []assertion
	}{
		{
			name:   "600519茅台_含2006股改停牌复合事件",
			klines: kl600519,
			events: ev600519,
			asserts: []assertion{
				// 股改前: 两个 ex-day(20060519 m=2.0 + 20060524 m=1.12)落在停牌缺口,
				// 必须复合成 m=2.24 施加到 20060425, 否则因子被污染。
				{20060425, -261.29, -259.88, -261.76, -260.48, "股改前(复合m=2.24)"},
				// 股改后首个交易日: 不应再受这两个事件影响。开盘为已知特例(见下方单独测试)。
				{20060526, naN, -260.58, -262.33, -260.60, "股改后"},
				// 近期: 纯派现累积。
				{20250626, naN, naN, naN, 1396.04, "纯派现累积"},
			},
		},
		{
			name:   "000651格力_含配股与2015大比例送转停牌",
			klines: kl000651,
			events: ev000651,
			asserts: []assertion{
				{20150702, naN, naN, naN, 5.77, "10送10派30前一日(舍入边界5.7650->5.77)"},
				{20150703, naN, naN, naN, 3.00, "除权日"},
			},
		},
	}

	for _, tc := range cases {
		t.Run(tc.name, func(t *testing.T) {
			xs := buildXRXDs(tc.events)
			ks := buildKlines(tc.klines)
			fs := xs.Pre(ks).Factors()
			byDate := factorByDate(fs)
			raw := make(map[int]fixKline, len(tc.klines))
			for _, k := range tc.klines {
				raw[k.date] = k
			}
			for _, a := range tc.asserts {
				f := byDate[a.date]
				if f == nil {
					t.Fatalf("%d 未生成因子", a.date)
				}
				k := raw[a.date]
				got := [4]Price{
					f.QFQPrice(Price(k.o)),
					f.QFQPrice(Price(k.h)),
					f.QFQPrice(Price(k.l)),
					f.QFQPrice(Price(k.c)),
				}
				exp := [4]float64{a.o, a.h, a.l, a.c}
				names := [4]string{"开", "高", "低", "收"}
				for i := 0; i < 4; i++ {
					if exp[i] == naN {
						continue
					}
					if !approxYuan(got[i], exp[i]) {
						t.Errorf("%d %s前复权: got %.2f, 通达信 %.2f  (%s)",
							a.date, names[i], got[i].Float64(), exp[i], a.note)
					}
				}
			}
		})
	}
}

// TestFactors_QFQ_KnownException 记录唯一已知无法对齐的特例:
// 600519 2006-05-26 开盘, 本算法=-260.97, 通达信桌面端=-260.98(差 0.005)。
// 原因见 PreKlines.Factors 注释。此测试断言本算法稳定输出 -260.97, 防止意外回归;
// 若将来定位到通达信该日确切口径, 再更新此处。
func TestFactors_QFQ_KnownException(t *testing.T) {
	xs := buildXRXDs(ev600519)
	ks := buildKlines(kl600519)
	fs := xs.Pre(ks).Factors()
	f := factorByDate(fs)[20060526]
	if f == nil {
		t.Fatal("20060526 未生成因子")
	}
	got := f.QFQPrice(Price(39490)) // 该日不复权开盘 39.49 元
	if !approxYuan(got, -260.97) {
		t.Errorf("已知特例点漂移: 600519 20060526 开盘 got %.2f, 期望本算法 -260.97 (通达信 -260.98)",
			got.Float64())
	}
}

// TestFactors_HFQ_Affine 验证后复权由前复权系数(最早日锚定)反推的一致性。
func TestFactors_HFQ_Affine(t *testing.T) {
	xs := buildXRXDs(ev000651)
	ks := buildKlines(kl000651)
	fs := xs.Pre(ks).Factors()
	if len(fs) == 0 {
		t.Fatal("无因子")
	}
	a0, b0 := fs[0].QFQMul, fs[0].QFQAdd
	for _, f := range fs {
		want := roundHalfUpYuan((f.QFQMul*f.Last.Float64() + f.QFQAdd - b0) / a0)
		got := f.HFQPrice(f.Last)
		if got != want {
			t.Errorf("%s HFQ 不一致: got %v want %v", f.Time.Format(time.DateOnly), got, want)
		}
	}
}

// TestFactors_FutureExDayIgnored 回归: gbbq 含【已公告但 ex-day 在未来】的除权记录时,
// 不得提前施加, 否则前复权锚定日 B!=0, 整条历史被平移一个未发生的分红额。
// 还原 600887 场景: 最新交易日为 2016-01-05, gbbq 却有一条 ex-day 在未来的派9元记录。
func TestFactors_FutureExDayIgnored(t *testing.T) {
	ks := buildKlines([]fixKline{
		{20160104, 15170, 15170, 12000, 15170}, // 收 15.17 元
		{20160105, 15490, 15490, 12000, 15490}, // 最新交易日
	})
	// 仅一条事件, 且 ex-day 晚于最新交易日(模拟"已公告未除权")
	xs := buildXRXDs([]fixEvent{
		{20160605, 9.0, 0.0, 0.0, 0.0}, // 未来派9元 => 若误施加会令 B=-0.90
	})
	fs := xs.Pre(ks).Factors()
	byDate := factorByDate(fs)
	// 未来事件未生效: 锚定日因子应为 A=1,B=0, 前复权价=不复权价。
	if f := byDate[20160105]; f == nil || !approxYuan(f.QFQPrice(Price(15490)), 15.49) {
		t.Errorf("未来 ex-day 被错误施加: 20160105 前复权收 got %.2f, 期望 15.49", byDate[20160105].QFQPrice(Price(15490)).Float64())
	}
	if f := byDate[20160104]; f == nil || !approxYuan(f.QFQPrice(Price(15170)), 15.17) {
		t.Errorf("未来 ex-day 被错误施加: 20160104 前复权收 got %.2f, 期望 15.17", byDate[20160104].QFQPrice(Price(15170)).Float64())
	}
}

